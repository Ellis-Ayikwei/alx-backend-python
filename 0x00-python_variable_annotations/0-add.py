#!/usr/bin/python3
"""
function add that takes a float a and a float b as arguments and
returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Calculates the sum of two floating point numbers.
    
    Args:
        a (float): The first number to be added.
        b (float): The second number to be added.
    
    Returns:
        float: The sum of the two numbers.
    """
    
    # Add the two numbers and convert the result to a float.
    return float(a + b)
