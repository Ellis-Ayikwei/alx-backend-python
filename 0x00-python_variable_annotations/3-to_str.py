#!/usr/bin/env python3
"""function to_str that takes a float n as argument and returns the string
representation of the float."""


def to_str(n:  float) -> str:
    """
    Returns the string representation of a float number.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float number.
    """
    return str(n)
